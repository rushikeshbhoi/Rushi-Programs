#include<iostream>
using namespace std;
int main()
{   
    int a;  //declaration
    a = 4;  //initialization
    cout<<"The size of int is "<<sizeof(a)<<endl;

    float b = 3.5;
    cout<<"The size of float is "<<sizeof(b)<<endl;

    char c ;
    cout<<"The size of char is "<<sizeof(c)<<endl;

    bool d = true;
    cout<<"The size of bool is "<<sizeof(d)<<endl;


    return 0;
}